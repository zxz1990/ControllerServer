package com.zxz.controllerserver.bean;

public class ServerRequestVo {
	
	/**
	 * 请求类型
	 */
	private String type;
	
	/**
	 * 请求参数
	 */
	private String params;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getParams() {
		return params;
	}

	public void setParams(String params) {
		this.params = params;
	}

	
	
}
